#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char *d;
	char *s;

	d = dst;
	s = (char*)src;
	if (dst == src)
		return (dst);
	if (d > s)
	{
		while (len--)
			d[len] = s[len];
		return (dst);
	}
	while (len--)
		*d++ = *s++;
	return (dst);
}
