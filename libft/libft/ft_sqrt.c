#include "libft.h"

int		ft_sqrt(int nb)
{
	int x;
	int rsl;

	x = 1;
	rsl = 0;
	if (nb <= 0)
		return (0);
	while (rsl <= nb)
	{
		rsl = x * x;
		if (rsl == nb)
			return (x);
		x++;
	}
	return (0);
}
