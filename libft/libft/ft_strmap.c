#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		i;

	i = 0;
	if (s == NULL || *f == NULL)
		return (0);
	while (s[i])
		i++;
	if ((str = (char *)malloc(sizeof(*str) * (i + 1))) == NULL)
		return (0);
	i = 0;
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
